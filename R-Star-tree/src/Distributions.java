public class Distributions {

    private DistributionGroup groupA;
    private DistributionGroup groupB;

    public Distributions(DistributionGroup groupA, DistributionGroup groupB) {
        this.groupA = groupA;
        this.groupB = groupB;
    }

    public DistributionGroup getGroupA() {
        return groupA;
    }

    public DistributionGroup getGroupB() {
        return groupB;
    }
}
